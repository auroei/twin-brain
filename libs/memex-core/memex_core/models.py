# Atomic Memory Models (Phase 1 & 2 Enhancement)
# =============================================================================

class MemoryRelation(str, Enum):
    """Types of relationships between memories."""
    UPDATES = "updates"      # New info contradicts/replaces old (version history)
    EXTENDS = "extends"      # New info supplements without contradiction
    DERIVES = "derives"      # Inferred from combining multiple memories
    NONE = "none"           # Standalone memory


class TemporalMetadata(BaseModel):
    """Dual-layer temporal grounding for memories."""
    document_date: datetime = Field(
        ..., 
        description="When the source conversation/document was created"
    )
    event_dates: List[datetime] = Field(
        default_factory=list,
        description="When the events being discussed actually occurred/will occur"
    )
    is_future_event: bool = Field(
        default=False,
        description="Whether this discusses a future event (planning, roadmap)"
    )
    temporal_references: List[str] = Field(
        default_factory=list,
        description="Raw temporal phrases extracted: 'last week', 'Q2', 'by March'"
    )


class AtomicMemory(BaseModel):
    """
    Single atomic fact extracted from a thread.
    
    Key insight: Standard RAG retrieves raw chunks that lack context when isolated.
    Atomic memories are self-contained facts with resolved references.
    
    Example:
    - Bad chunk: "He said we should go with option B"
    - Atomic memory: "John recommended Option B (mobile-first approach) for the Q3 launch"
    """
    id: str = Field(..., description="Unique memory ID: mem_{thread_ts}_{index}")
    fact: str = Field(
        ..., 
        description="Self-contained atomic fact with resolved references"
    )
    source_thread_ts: str = Field(..., description="Parent thread timestamp")
    source_chunk_content: str = Field(
        default="",
        description="Original chunk text for context retrieval"
    )
    chunk_index: int = Field(..., description="Position in source thread")
    
    # Entity extraction
    entities: List[str] = Field(
        default_factory=list,
        description="People, projects, products mentioned: ['John', 'Visa Infinite', 'CC']"
    )
    
    # Temporal grounding
    temporal: Optional[TemporalMetadata] = None
    
    # Relationships
    relation_type: MemoryRelation = Field(default=MemoryRelation.NONE)
    related_memory_ids: List[str] = Field(
        default_factory=list,
        description="IDs of memories this updates/extends/derives from"
    )
    is_latest: bool = Field(
        default=True,
        description="For update chains, is this the current version?"
    )
    
    # Metadata
    confidence: float = Field(
        default=1.0, 
        ge=0.0, 
        le=1.0,
        description="Extraction confidence score"
    )
    created_at: datetime = Field(default_factory=datetime.now)


class MemoryExtractionResult(BaseModel):
    """Result of extracting atomic memories from a thread."""
    thread_ts: str
    memories: List[AtomicMemory]
    extraction_metadata: dict = Field(default_factory=dict)

