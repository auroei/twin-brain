"""
LLM-as-judge evaluation for end-to-end RAG quality.
Uses an LLM to judge if generated answers match ideal answers.
"""

from typing import List, Dict, Any, Optional


def judge_answer(
    question: str,
    ideal_answer: str,
    generated_answer: str,
    judge_client,
) -> Dict[str, Any]:
    """
    Use LLM to judge if a generated answer correctly addresses the question.
    
    Args:
        question: The original question
        ideal_answer: The expected/reference answer
        generated_answer: The answer generated by the RAG system
        judge_client: GeminiClient (or compatible) for making judgment calls
        
    Returns:
        Dictionary containing:
        - is_correct: Boolean indicating if answer was judged correct
        - judgment: The LLM's judgment text
        - error: Error message if judgment failed (optional)
    """
    judge_prompt = f"""You are evaluating whether a generated answer correctly addresses a question compared to an ideal answer.

<question>
{question}
</question>

<ideal_answer>
{ideal_answer}
</ideal_answer>

<generated_answer>
{generated_answer}
</generated_answer>

Does the generated answer correctly address the key points from the ideal answer? 
Consider:
- Factual accuracy
- Coverage of main points
- Relevance to the question

Respond with ONLY "CORRECT" or "INCORRECT" followed by a brief explanation."""

    try:
        judgment = judge_client.call_with_retry(judge_prompt)
        is_correct = judgment.strip().upper().startswith("CORRECT")
        
        return {
            "is_correct": is_correct,
            "judgment": judgment.strip()
        }
    except Exception as e:
        return {
            "is_correct": False,
            "judgment": "",
            "error": str(e)
        }


def evaluate_end_to_end(
    retrieval_pipeline,
    eval_data: List[Dict[str, Any]],
    role_def,
    judge_client,
    behavior_config: Optional[Dict] = None,
    retrieval_config: Optional[Dict] = None,
    verbose: bool = True
) -> Dict[str, Any]:
    """
    Evaluate end-to-end answer quality using LLM-as-judge.
    
    For each question:
    1. Generate an answer using the retrieval pipeline
    2. Use LLM to judge if the generated answer matches the ideal answer
    
    Args:
        retrieval_pipeline: RetrievalPipeline instance for generating answers
        eval_data: List of evaluation items with:
            - question: The test question
            - ideal_answer: Expected answer content
        role_def: Role definition for answer generation
        judge_client: GeminiClient for judging answer quality
        behavior_config: Optional behavior configuration
        retrieval_config: Optional retrieval configuration
        verbose: Whether to print per-question results
        
    Returns:
        Dictionary containing:
        - accuracy: Percentage of answers judged as correct
        - correct_count: Number of correct answers
        - total_count: Total number of evaluated items
        - results: List of per-item results (if verbose)
    """
    correct_count = 0
    total_count = 0
    results = []
    
    if verbose:
        print(f"\n📊 Evaluating End-to-End Quality...")
        print("-" * 60)
    
    for i, item in enumerate(eval_data, 1):
        question = item.get("question", "")
        ideal_answer = item.get("ideal_answer", "")
        
        if not ideal_answer:
            if verbose:
                print(f"  ⚠️  Skipping item {i}: No ideal_answer provided")
            continue
        
        total_count += 1
        
        # Generate answer using the pipeline
        try:
            generated_answer = retrieval_pipeline.answer_question(
                query=question,
                role_def=role_def,
                behavior_config=behavior_config,
                retrieval_config=retrieval_config,
                n_results=10
            )
        except Exception as e:
            if verbose:
                print(f"  ❌ Q{i}: Error generating answer: {e}")
            results.append({
                "question": question,
                "generated": "",
                "ideal": ideal_answer,
                "is_correct": False,
                "error": str(e)
            })
            continue
        
        # Judge the answer
        judgment_result = judge_answer(
            question=question,
            ideal_answer=ideal_answer,
            generated_answer=generated_answer,
            judge_client=judge_client
        )
        
        is_correct = judgment_result.get("is_correct", False)
        if is_correct:
            correct_count += 1
        
        if verbose:
            status = "✅" if is_correct else "❌"
            print(f"  Q{i}: {status} | {question[:50]}...")
        
        results.append({
            "question": question,
            "generated": generated_answer[:200] + "..." if len(generated_answer) > 200 else generated_answer,
            "ideal": ideal_answer[:200] + "..." if len(ideal_answer) > 200 else ideal_answer,
            "is_correct": is_correct,
            "judgment": judgment_result.get("judgment", "")[:200]
        })
    
    accuracy = (correct_count / total_count * 100) if total_count > 0 else 0.0
    
    return {
        "accuracy": accuracy,
        "correct_count": correct_count,
        "total_count": total_count,
        "results": results if verbose else []
    }

